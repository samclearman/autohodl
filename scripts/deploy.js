async function main() {
  const [deployer] = await ethers.getSigners();

  console.log("Deploying contracts with the account:", deployer.address);

  console.log("Account balance:", (await deployer.getBalance()).toString());

  const auctioFactory = await ethers.getContractFactory("Autohodl");
  const auctioContract = await auctioFactory.deploy();

  console.log("Contract address:", auctioContract.address);
}

main()
  .then(() => process.exit(0))
  .catch((error) => {
    console.error(error);
    process.exit(1);
  });
